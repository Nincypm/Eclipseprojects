package Controlstatements;

public class Elseifstmnt {

	public static void main(String[] args) {

		int a=10,b=15,c=20;
		if(a>b)
		{
			System.out.println("a is greater");
		}
		else if(a>c & b>c)
		{
			System.out.println("b is greater");
		}
		else
		{
			System.out.println("c is greater");
		}
			
	}

}
